package co.za.izinga.menuupdater.kfc.model;

public class Image {
    private String mainImage;
    private String secondaryImage;
    private String tertiaryImage;

    public String getMainImage() {
        return mainImage;
    }

    public void setMainImage(String value) {
        this.mainImage = value;
    }

    public String getSecondaryImage() {
        return secondaryImage;
    }

    public void setSecondaryImage(String value) {
        this.secondaryImage = value;
    }

    public String getTertiaryImage() {
        return tertiaryImage;
    }

    public void setTertiaryImage(String value) {
        this.tertiaryImage = value;
    }
}
